function square(x) {
    return x * x;
  }
  
  function sum(x, y) {
    return x + y;
  }
  
  function isPositive(x) {
    return x > 0;
  }
  
  function calculateArea(width, height) {
    return width * height;
  }
  
  function calculateVolume(length, width, height) {
    return length * width * height;
  }
  
  function convertCelsiusToFahrenheit(celsius) {
    return (celsius * 9) / 5 + 32;
  }
  